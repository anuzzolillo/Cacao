<?php

	/**
	* _________________________________________________________________________________________
	* 
	*  Verify some fields
	* _________________________________________________________________________________________
	**/
	if(isset($_POST["title"])) {

	/**
	* _________________________________________________________________________________________
	* 
	*  Require Resources
	* _________________________________________________________________________________________
	**/
	require("../../Config/Config.php");

	/**
	* _________________________________________________________________________________________
	* 
	*  Call file class
	* _________________________________________________________________________________________
	**/
	$file = new Models\File;

	/**
	* _________________________________________________________________________________________
	* 
	*  Set keys
	* _________________________________________________________________________________________
	**/
	$file->set("title", $_POST["title"]);
	$file->set("alt", $_POST["title"]);
	$file->set("time", date("Y-m-d h:i:s"));
	$file->set("user", $_SESSION["id"]);
	$file->catchFile($_FILES["file"]);

	/**
	* _________________________________________________________________________________________
	* 
	*  Upload file
	* _________________________________________________________________________________________
	**/
	$file->upload();
	
	/**
	* _________________________________________________________________________________________
	* 
	*  End
	* _________________________________________________________________________________________
	**/
	} else {
		echo 0;
	}

?>