<?php

	/**
	* _________________________________________________________________________________________
	* 
	*  Verify some fields
	* _________________________________________________________________________________________
	**/
	if(isset($_POST["title"]) AND isset($_POST["description"])) {

	/**
	* _________________________________________________________________________________________
	* 
	*  Require Resources
	* _________________________________________________________________________________________
	**/
	require("../../Config/Config.php");

	/**
	* _________________________________________________________________________________________
	* 
	*  Call page class
	* _________________________________________________________________________________________
	**/
	$page = new Models\Page;

	/**
	* _________________________________________________________________________________________
	* 
	*  Set keys
	* _________________________________________________________________________________________
	**/
	$page->set("title", $_POST["title"]);
	$page->set("description", $_POST["description"]);
	$page->set("owner", $_POST["owner"]);
	$page->set("email", $_POST["email"]);
	$page->set("articles", $_POST["articles"]);
	$page->set("status", $_POST["status"] ?? 1);

	/**
	* _________________________________________________________________________________________
	* 
	*  Update data
	* _________________________________________________________________________________________
	**/
	$page->update();
	echo 1;

	/**
	* _________________________________________________________________________________________
	* 
	*  End
	* _________________________________________________________________________________________
	**/
	} else {
		echo 0;
	}

?>